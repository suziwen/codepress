<h3>About CodePress</h3>
<p>
	I began to write this code mainly because of another project called <a href=http://ecco.sourceforge.net/>ECCO</a>.
</p>
<p>
	I needed a good editor to make possible to build a web-based IDE with at least the most common features a desktop IDE have. Then I created a 
	JavaScript syntax highligting engine called RTSHJS. 
</p>
<p>	
	As ECCO development goes on, I decided to release RTSHJS as a separated project called CodePress. I intend to go back to ECCO development when 
	CodePress reaches version 1.0.<br>
</p>
<p>	
	Since CodePress version 0.9, Michael Hurni has joined the project bringing some new features like code snippets and completion and fixes to CodePress core.<br>
</p>
<p>
